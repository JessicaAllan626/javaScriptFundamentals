//! Equal To
//* compares the values of two or more things

'3' == 3;
console.log('3' == 3);

//! Strict Equal To
//* compares the values and Datatypes to check if they are equal

console.log(3 === 3); //true
console.log(3 === '3'); //false

//! Not Equal To 
//* compares the values of two or more things to see if they are not equal
console.log('3' != 3); //false
console.log(3 != 3); //false

//! Strict Not Equal To 
//* compares the values and Datatypes to check if they are not equal 
'3' !== 3; 

//! Greater than 
3 > 2;

//! Less Than 
2 < 3;

//! Greater Than or Equal To
//* Must be the Greater than symbol first and then the equal sign
3 >= 2; //not to be confused with => (fat arrow functions, we will ge into this later)

//! Less Than or Equal To
2 <= 3;

//! And operator
2 && 3;

//! Or operator
2 || 3; 


